import Foundation
import UIKit

class AppFont {
    
    static func bold(size: CGFloat) -> UIFont {
        UIFont(name: "IBMPlexSansArabic-Bold", size: size)!
    }
    
    static func extraLight(size: CGFloat) -> UIFont {
        UIFont(name: "IBMPlexSansArabic-ExtraLight", size: size)!
    }
    
    static func light(size: CGFloat) -> UIFont {
        UIFont(name: "IBMPlexSansArabic-Light", size: size)!
    }
    
    static func medium(size: CGFloat) -> UIFont {
        UIFont(name: "IBMPlexSansArabic-Medium", size: size)!
    }
    
    static func regular(size: CGFloat) -> UIFont {
        UIFont(name: "IBMPlexSansArabic-Regular", size: size)!
    }
    
    static func semiBold(size: CGFloat) -> UIFont {
        UIFont(name: "IBMPlexSansArabic-SemiBold", size: size)!
    }
    
    static func text(size: CGFloat) -> UIFont {
        UIFont(name: "IBMPlexSansArabic-Text", size: size)!
    }
    
    static func thin(size: CGFloat) -> UIFont {
        UIFont(name: "IBMPlexSansArabic-Thin", size: size)!
    }
}
